/*
    - 6° AULA - OPERADORES DE COMPARAÇÃO PARTE 2: 

        > -> Maior.
        < -> Menor.

        >= -> Maior ou Igual.
        <= -> Menor ou Igual.

*/
// Exemplo de Menor(<), ou Maior(>).
const numberOne = 10
const numberTwo = 20

// Perguntando se é maior..
console.log(numberOne > numberTwo);

// Perguntando se é menor..
console.log(numberOne < numberTwo);



// Exemplo de Menor ou igual(<=), ou Maio ou igual(>=)
const numberThree = 10
const numberFour = 10

// Perguntando se é maior ou igual.
console.log(numberThree >= numberFour);

// Perguntando se é menor ou igual.
console.log(numberThree <= numberFour);
