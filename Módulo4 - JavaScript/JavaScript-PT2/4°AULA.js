/*
    - 4° AULA - OPERADORES DE ATRIBUIÇÃO:

        = -> Atribuição.
        += -> Adição.
        -+ -> Subtração.
        /+ -> Divisão.
        *= -> Multiplicação.
        %= -> Resto.

*/

// Atribuição.
const atribuçao = 20 // Onde o "=", atribui o número 20 a varavel.

// Adição.
let adiçao = 20
adiçao += 70 // isso aqui é a mesma coisa que isso -> adiçao = adiçao + 70.

// Subtração.
let subtraçao = 20
subtraçao -= 10 // isso aqui é a mesma coisa que isso -> subtraçao = subtraçao - 10.

// Divisão.
let divisao = 20
divisao /= 2 // isso aqui é a mesma coisa que isso -> divisao = divisao / 2.

// Multiplicação.
let multiplicaçao = 20
multiplicaçao *= 2 // isso aqui é a mesma coisa que isso -> multiplicaçao = multiplicaçao * 2.

// Resto.
let resto = 20
resto %=2 // isso aqui é a mesma coisa que isso -> resto = resto %c 2.