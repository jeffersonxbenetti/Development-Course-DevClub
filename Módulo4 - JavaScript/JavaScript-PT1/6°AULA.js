/*  - 6° AULA - O QUE SÃO VARIÁVEIS?

    - Variáveis são espaços de memoria onde você guarda um valor, objeto, img, texto e etc..

    - Existem três formas para se fazer as variáveis que são:
        - let
        - const
        - var
                
    - Diferença entre elas simplificada.    
        - var -> DESCONTINUADA, não é mais usada.
        - let -> Variável onde se pode trocar o valor declarado.
        - const -> Valor declarado que é constante, não pode ser trocado.

    - Podemos dar nomes as variáveis.
        - Exemplos de nomes que não pode:
            - Não pode ter espaços entre as palavras.
            - Não pode começar com número.

        - Exemplo de nome que seguirei:
            - nomeDaVariavel

    - Não podemos dar o mesmo nome para mais de uma variável.

*/

